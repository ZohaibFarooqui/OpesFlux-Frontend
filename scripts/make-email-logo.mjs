// Run once: node scripts/make-email-logo.mjs
// Creates public/logo-email.png (small, optimised for email) and
// src/lib/email-logo-b64.ts (base64 string embedded directly in email HTML)
import sharp from "sharp";
import fs from "fs";
import path from "path";

const src = path.join(process.cwd(), "public/logo.png");
const dst = path.join(process.cwd(), "public/logo-email.png");
const tsOut = path.join(process.cwd(), "src/lib/email-logo-b64.ts");

await sharp(src)
  .resize(120, 120, { fit: "inside", background: { r: 255, g: 255, b: 255, alpha: 0 } })
  .png({ compressionLevel: 9, quality: 80 })
  .toFile(dst);

const bytes = fs.readFileSync(dst);
const b64 = bytes.toString("base64");
const dataUri = `data:image/png;base64,${b64}`;

fs.writeFileSync(tsOut, `// Auto-generated by scripts/make-email-logo.mjs — do not edit\nexport const emailLogoDataUri = "${dataUri}";\n`);

console.log(`logo-email.png: ${(fs.statSync(dst).size / 1024).toFixed(1)} KB`);
console.log(`email-logo-b64.ts: ${(fs.statSync(tsOut).size / 1024).toFixed(1)} KB`);
